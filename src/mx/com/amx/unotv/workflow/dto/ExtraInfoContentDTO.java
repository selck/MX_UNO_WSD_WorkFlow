package mx.com.amx.unotv.workflow.dto;

public class ExtraInfoContentDTO {
	
	private String url_nota;
	private String ruta_dfp;
    private String desc_categoria;
    private String desc_seccion;
    
	/**
	 * @return the url_nota
	 */
	public String getUrl_nota() {
		return url_nota;
	}
	/**
	 * @param url_nota the url_nota to set
	 */
	public void setUrl_nota(String url_nota) {
		this.url_nota = url_nota;
	}
	/**
	 * @return the ruta_dfp
	 */
	public String getRuta_dfp() {
		return ruta_dfp;
	}
	/**
	 * @param ruta_dfp the ruta_dfp to set
	 */
	public void setRuta_dfp(String ruta_dfp) {
		this.ruta_dfp = ruta_dfp;
	}
	/**
	 * @return the desc_categoria
	 */
	public String getDesc_categoria() {
		return desc_categoria;
	}
	/**
	 * @param desc_categoria the desc_categoria to set
	 */
	public void setDesc_categoria(String desc_categoria) {
		this.desc_categoria = desc_categoria;
	}
	/**
	 * @return the desc_seccion
	 */
	public String getDesc_seccion() {
		return desc_seccion;
	}
	/**
	 * @param desc_seccion the desc_seccion to set
	 */
	public void setDesc_seccion(String desc_seccion) {
		this.desc_seccion = desc_seccion;
	}
    
    
}
